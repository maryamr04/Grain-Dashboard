             do.call(tabsetPanel, c(
               id = "year_tabs",
               lapply(years, function(yr) {
                 tabPanel(
                   title = paste(yr),
                   lapply(categories, function(cat) {
                     safe_id <- paste0("soy_progress_", yr, "_", gsub("[^A-Za-z]", "_", cat))
                     tagList(
                       h4(paste(cat, "—", yr)),
                       withSpinner(
                         plotlyOutput(safe_id, height = "300px"),
